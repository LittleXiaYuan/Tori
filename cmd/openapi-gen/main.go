// openapi-gen — Scan gateway routes and emit an OpenAPI 3.1.0 spec skeleton.
//
// Usage:
//
//	go run ./cmd/openapi-gen [gatewayDir] [outputPath]
//
// Defaults:
//   - gatewayDir:  internal/controlplane/gateway
//   - outputPath:  docs/openapi.yaml
//
// The generator extracts every mux.HandleFunc("path", ...) registration in the
// gateway package and produces a path-only spec with sensible defaults
// (security, tags, response codes). Schema bodies are intentionally left as
// `type: object` placeholders — flesh them out per endpoint as needed.
package main

import (
	"fmt"
	"os"
	"path/filepath"
	"regexp"
	"sort"
	"strings"

	"gopkg.in/yaml.v3"
)

var routePattern = regexp.MustCompile(`mux\.HandleFunc\(\s*"([^"]+)"`)

type openAPISpec struct {
	OpenAPI    string                          `yaml:"openapi"`
	Info       openAPIInfo                     `yaml:"info"`
	Servers    []openAPIServer                 `yaml:"servers"`
	Tags       []openAPITag                    `yaml:"tags,omitempty"`
	Paths      map[string]map[string]operation `yaml:"paths"`
	Components openAPIComponents               `yaml:"components"`
}

type openAPIInfo struct {
	Title       string            `yaml:"title"`
	Description string            `yaml:"description"`
	Version     string            `yaml:"version"`
	License     map[string]string `yaml:"license,omitempty"`
}

type openAPIServer struct {
	URL         string `yaml:"url"`
	Description string `yaml:"description,omitempty"`
}

type openAPITag struct {
	Name        string `yaml:"name"`
	Description string `yaml:"description,omitempty"`
}

type operation struct {
	Tags        []string              `yaml:"tags,omitempty"`
	Summary     string                `yaml:"summary,omitempty"`
	OperationID string                `yaml:"operationId,omitempty"`
	Parameters  []parameter           `yaml:"parameters,omitempty"`
	RequestBody *requestBody          `yaml:"requestBody,omitempty"`
	Responses   map[string]response   `yaml:"responses"`
	Security    []map[string][]string `yaml:"security,omitempty"`
}

type parameter struct {
	Name        string         `yaml:"name"`
	In          string         `yaml:"in"`
	Required    bool           `yaml:"required,omitempty"`
	Schema      map[string]any `yaml:"schema"`
	Description string         `yaml:"description,omitempty"`
}

type requestBody struct {
	Required bool                 `yaml:"required,omitempty"`
	Content  map[string]mediaType `yaml:"content"`
}

type mediaType struct {
	Schema map[string]any `yaml:"schema"`
}

type response struct {
	Description string               `yaml:"description"`
	Content     map[string]mediaType `yaml:"content,omitempty"`
}

type openAPIComponents struct {
	Schemas         map[string]map[string]any `yaml:"schemas"`
	SecuritySchemes map[string]map[string]any `yaml:"securitySchemes"`
}

func main() {
	gatewayDir := filepath.Join("internal", "controlplane", "gateway")
	if len(os.Args) > 1 {
		gatewayDir = os.Args[1]
	}
	outputPath := filepath.Join("docs", "openapi.yaml")
	if len(os.Args) > 2 {
		outputPath = os.Args[2]
	}

	paths, err := extractPaths(gatewayDir)
	if err != nil {
		fmt.Fprintln(os.Stderr, "extract:", err)
		os.Exit(1)
	}
	if len(paths) == 0 {
		fmt.Fprintln(os.Stderr, "no routes found in", gatewayDir)
		os.Exit(1)
	}

	spec := buildSpec(paths)

	out, err := yaml.Marshal(spec)
	if err != nil {
		fmt.Fprintln(os.Stderr, "marshal:", err)
		os.Exit(1)
	}

	if err := os.MkdirAll(filepath.Dir(outputPath), 0o755); err != nil {
		fmt.Fprintln(os.Stderr, "mkdir:", err)
		os.Exit(1)
	}
	header := fmt.Sprintf("# Auto-generated by cmd/openapi-gen — DO NOT EDIT BY HAND.\n# Source: %s\n# Run `go run ./cmd/openapi-gen` to regenerate.\n\n", gatewayDir)
	if err := os.WriteFile(outputPath, append([]byte(header), out...), 0o644); err != nil {
		fmt.Fprintln(os.Stderr, "write:", err)
		os.Exit(1)
	}
	fmt.Printf("OK — %d paths (%d auto + %d manual extras) written to %s\n",
		len(spec.Paths), len(paths), len(extraPaths()), outputPath)
}

// shouldInclude decides whether a registered path is a real JSON API endpoint
// worth documenting in OpenAPI. We skip the catch-all UI route, static asset
// prefixes, and any path that doesn't begin with /api, /v1, /webhook, or /healthz.
func shouldInclude(p string) bool {
	if p == "" || p == "/" {
		return false
	}
	switch {
	case strings.HasPrefix(p, "/v1/"),
		strings.HasPrefix(p, "/api/"),
		strings.HasPrefix(p, "/webhook/"),
		p == "/healthz":
		return true
	}
	return false
}

func extractPaths(dir string) ([]string, error) {
	seen := map[string]bool{}
	err := filepath.WalkDir(dir, func(f string, d os.DirEntry, err error) error {
		if err != nil {
			return err
		}
		if d.IsDir() {
			return nil
		}
		if filepath.Ext(f) != ".go" || strings.HasSuffix(f, "_test.go") {
			return nil
		}
		data, err := os.ReadFile(f)
		if err != nil {
			return err
		}
		for _, m := range routePattern.FindAllStringSubmatch(string(data), -1) {
			p := m[1]
			if !shouldInclude(p) {
				continue
			}
			// Trailing-slash routes in net/http mux are subtree handlers — they
			// match `/prefix/<anything>`. Convert to an OpenAPI path parameter.
			if strings.HasSuffix(p, "/") && p != "/" {
				p = p + "{id}"
			}
			seen[p] = true
		}
		return nil
	})
	if err != nil {
		return nil, err
	}
	out := make([]string, 0, len(seen))
	for p := range seen {
		out = append(out, p)
	}
	sort.Strings(out)
	return out, nil
}

func buildSpec(paths []string) *openAPISpec {
	spec := &openAPISpec{
		OpenAPI: "3.1.0",
		Info: openAPIInfo{
			Title:       "Yunque Agent API",
			Description: "Yunque (云雀) — production AI agent runtime with declarative Cogni shells, multi-channel chat, RAG knowledge, task workflows, and governance.",
			Version:     "v1.0.0",
			License:     map[string]string{"name": "MIT"},
		},
		Servers: []openAPIServer{
			{URL: "http://localhost:9090", Description: "Local default"},
			{URL: "https://yunque.example.com", Description: "Production (replace with your host)"},
		},
		Paths: map[string]map[string]operation{},
		Components: openAPIComponents{
			Schemas: map[string]map[string]any{
				"Error": {
					"type": "object",
					"properties": map[string]any{
						"error": map[string]any{"type": "string", "description": "Error message"},
						"code":  map[string]any{"type": "string", "description": "Machine-readable code"},
					},
					"required": []string{"error"},
				},
				"OK": {
					"type": "object",
					"properties": map[string]any{
						"ok": map[string]any{"type": "boolean"},
					},
				},
				"Empty": {"type": "object"},
			},
			SecuritySchemes: map[string]map[string]any{
				"BearerAuth": {
					"type":         "http",
					"scheme":       "bearer",
					"bearerFormat": "JWT",
					"description":  "Use POST /v1/token to obtain a JWT, then send `Authorization: Bearer <token>`.",
				},
			},
		},
	}

	tagSet := map[string]string{}
	// 1. Auto-discovered paths (from route scanner).
	for _, p := range paths {
		tag := inferTag(p)
		tagSet[tag] = tagDescription(tag)

		methods := inferMethods(p)
		ops := map[string]operation{}
		for _, m := range methods {
			ops[m] = defaultOperation(p, m, tag, !isPublic(p))
		}
		spec.Paths[p] = ops
	}

	// 2. Extra paths (handler-internal subroutes the scanner can't see).
	// These take precedence: if an extra path matches an auto-generated
	// path, the extra entry replaces the auto-generated method.
	autoSubsumed := map[string]bool{} // paths the extras now own end-to-end
	for _, e := range extraPaths() {
		ops, ok := spec.Paths[e.Path]
		if !ok {
			ops = map[string]operation{}
			spec.Paths[e.Path] = ops
		}
		// If we're filling in this extra path for the first time, drop any
		// auto-generated default method entries for the same path so we don't
		// keep stale GET/POST placeholders next to precise overrides.
		if !autoSubsumed[e.Path] {
			for k := range ops {
				delete(ops, k)
			}
			autoSubsumed[e.Path] = true
		}
		op := defaultOperation(e.Path, e.Method, e.Tag, !isPublic(e.Path))
		op.Summary = e.Summary
		op.OperationID = e.OperationID
		ops[e.Method] = op
		tagSet[e.Tag] = tagDescription(e.Tag)
	}

	// 3. Schema overrides (precise request/response bodies for hand-curated
	//    endpoints). Applied last so they win over auto-generated placeholders
	//    AND extra-path defaults.
	for _, s := range schemaOverrides() {
		ops, ok := spec.Paths[s.Path]
		if !ok {
			continue
		}
		op, ok := ops[s.Method]
		if !ok {
			continue
		}
		if s.RequestSchema != nil {
			op.RequestBody = &requestBody{
				Required: true,
				Content: map[string]mediaType{
					"application/json": {Schema: s.RequestSchema},
				},
			}
		}
		if s.Response200 != nil {
			resp := op.Responses["200"]
			resp.Content = map[string]mediaType{
				"application/json": {Schema: s.Response200},
			}
			if s.Response200Desc != "" {
				resp.Description = s.Response200Desc
			}
			op.Responses["200"] = resp
		}
		ops[s.Method] = op
	}

	tags := make([]openAPITag, 0, len(tagSet))
	for name, desc := range tagSet {
		tags = append(tags, openAPITag{Name: name, Description: desc})
	}
	sort.Slice(tags, func(i, j int) bool { return tags[i].Name < tags[j].Name })
	spec.Tags = tags

	return spec
}

// inferTag picks a tag from the second path segment when present, otherwise
// the first. It also normalises a few aliases (api/skillhub → skillhub).
func inferTag(p string) string {
	parts := strings.Split(strings.Trim(p, "/"), "/")
	if len(parts) == 0 {
		return "default"
	}
	switch parts[0] {
	case "api":
		if len(parts) >= 2 {
			return parts[1]
		}
		return "api"
	case "v1":
		if len(parts) >= 2 {
			return parts[1]
		}
		return "v1"
	case "webhook":
		return "webhook"
	case "healthz":
		return "system"
	}
	return parts[0]
}

