// isPublic returns true for endpoints registered without requireAuth.
// This is best-effort; verify against the actual handler when generating SDKs.
func isPublic(p string) bool {
	switch {
	case p == "/healthz",
		p == "/v1/version",
		p == "/v1/token",
		strings.HasPrefix(p, "/webhook/"),
		strings.HasPrefix(p, "/v1/webchat/widget"):
		return true
	}
	return false
}

