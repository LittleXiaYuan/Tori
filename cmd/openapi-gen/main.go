func defaultOperation(path, method, tag string, auth bool) operation {
	op := operation{
		Tags:        []string{tag},
		Summary:     fmt.Sprintf("%s %s", strings.ToUpper(method), path),
		OperationID: makeOperationID(method, path),
		Parameters:  pathParams(path),
		Responses: map[string]response{
			"200": {
				Description: "Success",
				Content: map[string]mediaType{
					"application/json": {Schema: map[string]any{"type": "object"}},
				},
			},
			"400": {Description: "Bad request", Content: errContent()},
		},
	}
	if auth {
		op.Responses["401"] = response{Description: "Unauthorized", Content: errContent()}
	}
	op.Responses["500"] = response{Description: "Internal error", Content: errContent()}

	if method == "post" || method == "put" || method == "patch" {
		op.RequestBody = &requestBody{
			Required: false,
			Content: map[string]mediaType{
				"application/json": {Schema: map[string]any{"type": "object"}},
			},
		}
	}
	if auth {
		op.Security = []map[string][]string{{"BearerAuth": {}}}
	}
	return op
}

// pathParams extracts {name} placeholders from a path and returns them as
// path parameters with a generic string schema.
func pathParams(path string) []parameter {
	var out []parameter
	parts := strings.Split(path, "/")
	for _, p := range parts {
		if strings.HasPrefix(p, "{") && strings.HasSuffix(p, "}") && len(p) > 2 {
			name := p[1 : len(p)-1]
			out = append(out, parameter{
				Name:        name,
				In:          "path",
				Required:    true,
				Schema:      map[string]any{"type": "string"},
				Description: fmt.Sprintf("Path parameter `%s`", name),
			})
		}
	}
	return out
}

func errContent() map[string]mediaType {
	return map[string]mediaType{
		"application/json": {Schema: map[string]any{"$ref": "#/components/schemas/Error"}},
	}
}

func makeOperationID(method, path string) string {
	parts := strings.Split(strings.Trim(path, "/"), "/")
	cleaned := make([]string, 0, len(parts)+1)
	cleaned = append(cleaned, method)
	for _, p := range parts {
		p = strings.ReplaceAll(p, "-", "_")
		p = strings.ReplaceAll(p, ".", "_")
		p = strings.ReplaceAll(p, "{", "")
		p = strings.ReplaceAll(p, "}", "")
		if p == "" {
			continue
		}
		cleaned = append(cleaned, p)
	}
	return strings.Join(cleaned, "_")
}
